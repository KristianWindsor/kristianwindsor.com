			<h2>
				Scalable business model
			</h2>
			<p>
				From <a href="../running-a-clothing-brand-lessons-learned/">my last business</a> I realized the importance of scalability, so I put thought into that first. 
			</p>
			<p>
				Creating a video would require 3 people:
			</p>
			<ul>
				<li>
					Orchestrator – plan and execute the video shoot and drive the follow car
				</li>
				<li>
					Videographer – film everything
				</li>
				<li>
					Video editor – edit the video
				</li>
			</ul>
			<p>
				Getting videographers would be the easiest. Dozens of websites like <a href="https://www.upwork.com" target="_blank">Upwork</a> and <a href="https://www.freelancer.com" target="_blank">Freelancer</a> offer a consistent supply of videographers available at any time and any place.
			</p>
			<p>
				Outsourcing video editors wouldn't be too difficult either. There are several websites such as <a href="https://www.fiverr.com/hire/video-editing" target="_blank">Fiverr</a> with a rich supply of video editors.
			</p>
			<p>
				An orchestrator (the person actually making the video happen) is the only position that can't be feasibly outsourced. To enable people in this position to act autonomously, they would have full ownership of the videos produced and receive the majority of the profit from each video. Fullspeed would find the clients and direct them to the orchestrators to carry out the video shoot.
			</p>
			<img class="isgif" src="beamers.png" alt="BMW wheel angle" />
